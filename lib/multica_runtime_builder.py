def write_source_snapshots(adapter_root: Path, superpowers_root: Path, out: Path) -> None:
    copy_tree(superpowers_root, out / 'source' / 'superpowers')
    adapter_dst = out / 'source' / 'superpower-adapter'
    adapter_dst.mkdir(parents=True, exist_ok=True)
    for rel in ('manifest.json', 'adapter.config.example.jsonc'):
        src = adapter_root / rel
        if src.exists():
            copy_file(src, adapter_dst / rel)
    for rel in ('overlays', 'role-prd', 'wiki-template', 'mcp/shared-wiki'):
        src = adapter_root / rel
        if src.exists():
            copy_tree(src, adapter_dst / rel)


def superpowers_version(superpowers_root: Path) -> str | None:
    package_json = superpowers_root / 'package.json'
    if not package_json.is_file():
        return None
    try:
        return load_json(package_json).get('version')
    except json.JSONDecodeError:
        return None


def build(adapter_root: Path, superpowers_arg: str, out: Path) -> None:
    adapter_root = adapter_root.resolve()
    if not (adapter_root / 'manifest.json').is_file():
        raise BuildError(f'Missing adapter manifest in {adapter_root}')
    out = out.resolve()
    with tempfile.TemporaryDirectory(prefix='multica-runtime-build-') as tmp:
        superpowers_root, source_meta = resolve_superpowers_source(superpowers_arg, Path(tmp))
        reset_output(out)
        dist = out / 'dist'
        write_source_snapshots(adapter_root, superpowers_root, out)
        workflow_records = write_workflows(adapter_root, superpowers_root, dist)
        role_records = write_role_agents(adapter_root, superpowers_root, dist)
        validator_records = write_validator_scripts(dist)
        script_records = write_tool_layer(adapter_root, dist, validator_records)
        preflight_records = write_preflight_artifacts(dist)
        task_graph_records = write_task_graphs(dist)
        static_records = write_gates_triggers_schemas_mcp(dist)

        adapter_manifest = load_manifest(adapter_root)
        manifest = {
            'schemaVersion': 1,
            'name': RUNTIME_NAME,
            'generatedBy': GENERATED_BY,
            'generatedAt': datetime.now(timezone.utc).replace(microsecond=0).isoformat(),
            'adapter': {
                'name': adapter_manifest.get('name'),
                'version': adapter_manifest.get('version'),
                'adaptedSuperpowersVersion': adapter_manifest.get('adaptedSuperpowersVersion'),
            },
            'superpowers': {
                **source_meta,
                'version': superpowers_version(superpowers_root),
            },
            'runtimeRootEnv': 'MULTICA_SUPERPOWERS_RUNTIME_ROOT',
            'rootReplacement': {
                'from': ADAPTER_PLUGIN_ROOT_PLACEHOLDER,
                'to': MULTICA_TOOLS_ROOT_EXPR,
            },
            'requiredCapabilities': list(REQUIRED_CAPABILITIES),
            'optionalMcpServers': list(OPTIONAL_MCP_SERVERS),
            'workflows': workflow_records,
            'roleAgents': role_records,
            'roleAgentContracts': serializable_role_agent_contracts(),
            'toolScripts': script_records,
            'validatorScripts': validator_records,
            'preflightArtifacts': preflight_records,
            'taskGraphs': task_graph_records,
            'gates': static_records['gates'],
            'gateContracts': static_records['gateContracts'],
            'gateTransitionContract': static_records['gateTransitionContract'],
            'roleTaskContract': static_records['roleTaskContract'],
            'triggers': static_records['triggers'],
            'schemas': static_records['schemas'],
            'mcpExamples': static_records['mcpExamples'],
            'artifactContracts': static_records['artifactContracts'],
            'artifactStoreContract': static_records['artifactStoreContract'],
            'squadContracts': static_records['squadContracts'],
            'issueTemplates': static_records['issueTemplates'],
            'compatibilityCommands': static_records['compatibilityCommands'],
            'issueTemplateBindings': static_records['issueTemplateBindings'],
            'intentRouterRules': static_records['intentRouterRules'],
            'artifactNextActions': static_records['artifactNextActions'],
            'autopilots': static_records['autopilots'],
            'autopilotContracts': static_records['autopilotContracts'],
            'validators': static_records['validators'],
            'nativeSkillPatches': [
                {'skill': spec.skill, 'blockId': spec.block_id, 'relativePath': spec.relative_path.as_posix()}
                for spec in PATCHES
            ],
            'boundary': 'Multica workflows are user entrypoints. Adapter Python scripts are tool-runner internals.',
        }
        write_json(out / 'manifest.json', manifest)
    print(f'Built Multica Superpowers runtime at {out}')


def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument('superpowers_source', help='Local Superpowers source path or Git URL')
    parser.add_argument('adapter_root', help='Adapter repository root')
    parser.add_argument('out', help='Output runtime bundle directory')
    return parser.parse_args(argv)


def main(argv: list[str]) -> int:
    args = parse_args(argv)
    build(Path(args.adapter_root), args.superpowers_source, Path(args.out))
    return 0


if __name__ == '__main__':
    raise SystemExit(main(sys.argv[1:]))

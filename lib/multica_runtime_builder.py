class BuildError(SystemExit):
    pass


def load_json(path: Path) -> dict:
    return json.loads(path.read_text(encoding='utf-8'))


def write_text(path: Path, text: str, *, executable: bool = False) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text.rstrip() + '\n', encoding='utf-8')
    if executable:
        mode = path.stat().st_mode
        path.chmod(mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)


def write_json(path: Path, data: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, ensure_ascii=False, indent=2, sort_keys=True) + '\n', encoding='utf-8')


def is_git_url(value: str) -> bool:
    return value.startswith(('http://', 'https://', 'git@')) or value.endswith('.git')


def resolve_superpowers_source(source_arg: str, temp_root: Path) -> tuple[Path, dict]:
    if is_git_url(source_arg):
        clone_dir = temp_root / 'superpowers-source'
        subprocess.run(['git', 'clone', '--depth', '1', source_arg, str(clone_dir)], check=True)
        revision = subprocess.check_output(['git', '-C', str(clone_dir), 'rev-parse', 'HEAD'], text=True).strip()
        return clone_dir, {'source': source_arg, 'revision': revision, 'sourceType': 'git'}

    source = Path(source_arg).expanduser().resolve()
    if not source.is_dir():
        raise BuildError(f'Missing Superpowers source: {source}')
    revision = None
    if (source / '.git').is_dir():
        try:
            revision = subprocess.check_output(['git', '-C', str(source), 'rev-parse', 'HEAD'], text=True).strip()
        except subprocess.CalledProcessError:
            revision = None
    return source, {'source': str(source), 'revision': revision, 'sourceType': 'local'}


def ensure_output_can_be_replaced(out: Path) -> None:
    if not out.exists():
        return
    if not out.is_dir():
        raise BuildError(f'Output exists and is not a directory: {out}')
    manifest_path = out / 'manifest.json'
    if not manifest_path.is_file():
        if any(out.iterdir()):
            raise BuildError(f'Output directory is not empty and is not a generated Multica runtime: {out}')
        return
    try:
        manifest = load_json(manifest_path)
    except json.JSONDecodeError as exc:
        raise BuildError(f'Output manifest is invalid; refusing to replace {out}: {exc}') from exc
    if manifest.get('generatedBy') != GENERATED_BY:
        raise BuildError(f'Output directory was not generated by this builder; refusing to replace {out}')


def reset_output(out: Path) -> None:
    ensure_output_can_be_replaced(out)
    if out.exists():
        shutil.rmtree(out)
    out.mkdir(parents=True)


def copy_tree(src: Path, dst: Path) -> None:
    def ignore(_dir: str, names: list[str]) -> set[str]:
        ignored = {'.git', 'node_modules', '__pycache__', '.pytest_cache', '.mypy_cache', '.DS_Store'}
        return {name for name in names if name in ignored}

    if dst.exists():
        shutil.rmtree(dst)
    shutil.copytree(src, dst, ignore=ignore)


def copy_file(src: Path, dst: Path, *, executable: bool = False) -> None:
    if not src.is_file():
        raise BuildError(f'Missing source file: {src}')
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)
    if executable:
        mode = dst.stat().st_mode
        dst.chmod(mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)


def replace_runtime_root(text: str) -> str:
    return text.replace(ADAPTER_PLUGIN_ROOT_PLACEHOLDER, MULTICA_TOOLS_ROOT_EXPR)


def render_multica_patch_block(spec) -> str:
    body = replace_runtime_root(spec.content)
    return f'{spec.start_marker}\n{body.rstrip()}\n{spec.end_marker}\n'


def patched_upstream_skill(superpowers_root: Path, workflow_id: str) -> tuple[str, str]:
    source_path = superpowers_root / 'skills' / workflow_id / 'SKILL.md'
    if not source_path.is_file():
        raise BuildError(f'Missing upstream skill: {source_path}')
    text = source_path.read_text(encoding='utf-8')
    applied_blocks: list[str] = []
    for spec in PATCHES:
        if spec.skill != workflow_id:
            continue
        text, _ = strip_block(text, spec)
        anchor = find_anchor(text, spec)
        insert_at = text.find(anchor) + len(anchor)
        block = '\n' + render_multica_patch_block(spec) + '\n'
        text = text[:insert_at] + block + text[insert_at:]
        applied_blocks.append(spec.block_id)
    return replace_runtime_root(text), ', '.join(applied_blocks)


def yaml_scalar(value: str) -> str:
    return json.dumps(value, ensure_ascii=False)


def yaml_list(items: Iterable[str], indent: int = 4) -> str:
    prefix = ' ' * indent
    values = list(items)
    if not values:
        return f'{prefix}[]'
    return '\n'.join(f'{prefix}- {yaml_scalar(item)}' for item in values)


def yaml_block(text: str, indent: int = 2) -> str:
    prefix = ' ' * indent
    return '\n'.join(prefix + line if line else prefix for line in text.rstrip().splitlines())


def render_illegal_transition_rules() -> str:
    lines = ['rules:']
    for rule in ILLEGAL_TRANSITION_RULES:
        lines.append(f'  - id: {yaml_scalar(rule["id"])}')
        lines.append(f'    blocks: {yaml_scalar(rule["blocks"])}')
        lines.append('    requiresArtifacts:')
        lines.append(yaml_list(rule['requiresArtifacts'], indent=6))
        lines.append('    requiresGates:')
        lines.append(yaml_list(rule['requiresGates'], indent=6))
        lines.append(f'    description: {yaml_scalar(rule["description"])}')
    return '\n'.join(lines)


def workflow_prompt(adapter_root: Path, superpowers_root: Path, workflow) -> tuple[str, str]:
    if workflow.source_kind == 'superpowers-native-skill':
        return patched_upstream_skill(superpowers_root, workflow.workflow_id)
    source = adapter_root / workflow.source_path
    if not source.is_file():
        raise BuildError(f'Missing adapter workflow source: {source}')
    return replace_runtime_root(source.read_text(encoding='utf-8')), 'adapter-skill-source'



def write_tool_layer(adapter_root: Path, dist: Path, validator_records: list[dict]) -> list[dict]:
    script_records = []
    for rel in installed_paths(adapter_root):
        if not rel.startswith('scripts/'):
            continue
        src = adapter_root / 'overlays' / rel
        name = Path(rel).name
        dst = dist / 'tools' / 'scripts' / name
        copy_file(src, dst, executable=name.endswith('.py'))
        script_records.append({
            'id': Path(name).stem.replace('_', '-'),
            'path': f'scripts/{name}',
            'runtimePath': f'{MULTICA_TOOLS_ROOT_EXPR}/scripts/{name}',
            'cwd': 'target repository root unless the workflow says otherwise',
            'entrypointType': 'tool-runner-internal',
        })

    write_json(dist / 'tools' / 'tool-manifest.json', {
        'schemaVersion': 1,
        'generatedBy': GENERATED_BY,
        'boundary': 'Adapter Python scripts are Multica tool-runner internals, not user entrypoints.',
        'runtimeRootEnv': 'MULTICA_SUPERPOWERS_RUNTIME_ROOT',
        'scripts': script_records,
        'validatorScripts': validator_records,
    })
    return script_records



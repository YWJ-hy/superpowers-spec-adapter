def write_role_agents(adapter_root: Path, superpowers_root: Path, dist: Path) -> list[dict]:
    records = []
    agents_dir = dist / 'agents'

    for agent_id in TEMPLATE_ROLE_AGENT_IDS:
        text = agent_header(agent_id, 'multica-template', 'every invocation must be independent from prior invocations.')
        text += template_role_body(agent_id)
        write_text(agents_dir / f'{agent_id}.md', text)
        records.append({'agentId': agent_id, 'source': 'multica-template', 'file': f'dist/agents/{agent_id}.md'})

    for agent_id, rel_path in ADAPTER_ROLE_AGENT_PATHS.items():
        text = replace_runtime_root((adapter_root / rel_path).read_text(encoding='utf-8')).rstrip() + role_language_rule()
        write_text(agents_dir / f'{agent_id}.md', text)
        records.append({'agentId': agent_id, 'source': rel_path.as_posix(), 'file': f'dist/agents/{agent_id}.md'})

    lanhu_by_id = {config.agent_name: render_lanhu_agent(adapter_root, config) for config in ROLE_CONFIGS}
    for agent_id in GENERATED_LANHU_AGENT_IDS:
        text = replace_runtime_root(lanhu_by_id[agent_id]).rstrip() + role_language_rule()
        write_text(agents_dir / f'{agent_id}.md', text)
        records.append({'agentId': agent_id, 'source': 'sync_role_prd.render_agent', 'file': f'dist/agents/{agent_id}.md'})

    for agent_id, rel_path in UPSTREAM_ROLE_PROMPTS.items():
        source = superpowers_root / rel_path
        if not source.is_file():
            raise BuildError(f'Missing upstream role prompt: {source}')
        fresh = 'one task/review invocation per Multica task.'
        text = agent_header(agent_id, rel_path.as_posix(), fresh)
        text += '\n## Upstream Superpowers prompt\n\n'
        text += replace_runtime_root(source.read_text(encoding='utf-8')).rstrip() + '\n'
        if agent_id == 'code-reviewer':
            text += '\nFinal-code-reviewer is the whole-implementation mode of this same role, not a separate persona.\n'
        write_text(agents_dir / f'{agent_id}.md', text)
        records.append({'agentId': agent_id, 'source': rel_path.as_posix(), 'file': f'dist/agents/{agent_id}.md'})

    missing = sorted(set(EXPECTED_ROLE_AGENTS) - {record['agentId'] for record in records})
    if missing:
        raise BuildError(f'Missing generated role agents: {missing}')
    write_json(agents_dir / 'role-agent-contracts.json', {
        'schemaVersion': 1,
        'generatedBy': GENERATED_BY,
        'freshContext': 'required',
        'contracts': serializable_role_agent_contracts(),
    })
    return records



CATEGORIES = ('artifactChecks', 'capabilityChecks', 'gateChecks', 'mcpChecks', 'illegalTransitionChecks')


def load_json(path: Path) -> dict:
    return json.loads(path.read_text(encoding='utf-8'))


def add(checks: dict[str, list[dict]], category: str, check_id: str, status: str, message: str, **extra) -> None:
    record = {'id': check_id, 'status': status, 'message': message}
    record.update(extra)
    checks[category].append(record)


def enum_values(schema: dict, *path: str) -> set[str]:
    node = schema
    for key in path:
        node = node.get(key, {}) if isinstance(node, dict) else {}
    return set(node.get('enum', [])) if isinstance(node, dict) else set()


def artifact_types(invocation: dict) -> set[str]:
    values = set()
    for item in invocation.get('sourceArtifacts', []):
        if isinstance(item, dict) and isinstance(item.get('type'), str):
            values.add(item['type'])
    return values


def gate_values(invocation: dict, key: str) -> set[str]:
    gates = invocation.get('gates', {})
    if not isinstance(gates, dict):
        return set()
    return {item for item in gates.get(key, []) if isinstance(item, str)}


def mcp_names(invocation: dict) -> set[str]:
    values = set()
    for item in invocation.get('mcpRequirements', []):
        if isinstance(item, dict) and isinstance(item.get('name'), str):
            values.add(item['name'])
    return values


def validate(argv: list[str]) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument('runtime_root')
    parser.add_argument('workflow_invocation')
    parser.add_argument('--available-capability', action='append', default=[])
    parser.add_argument('--available-mcp', action='append', default=[])
    args = parser.parse_args(argv)

    root = Path(args.runtime_root).expanduser().resolve()
    invocation_path = Path(args.workflow_invocation).expanduser().resolve()
    checks = {category: [] for category in CATEGORIES}

    schema = load_json(root / 'dist' / 'schemas' / 'workflow-invocation.schema.json')
    contract = load_json(root / 'dist' / 'preflight' / 'workflow-invocation-contract.json')
    invocation = load_json(invocation_path)

    for field in schema.get('required', []):
        if field in invocation:
            add(checks, 'artifactChecks', f'top-level-field:{field}', 'passed', 'Required top-level field is present.', field=field)
        else:
            add(checks, 'artifactChecks', f'top-level-field:{field}', 'blocked', 'Required top-level field is missing.', field=field)

    workflow_id = invocation.get('workflowId')
    trigger_source = invocation.get('triggerSource')
    execution_mode = invocation.get('executionMode')
    actual_artifacts = artifact_types(invocation)
    required_gates = gate_values(invocation, 'required')
    satisfied_gates = gate_values(invocation, 'satisfied')
    actual_capabilities = set(invocation.get('requiredCapabilities', [])) if isinstance(invocation.get('requiredCapabilities'), list) else set()
    available_capabilities = set(args.available_capability)
    available_mcp = set(args.available_mcp)
    actual_mcp = mcp_names(invocation)
    issue_template_id = invocation.get('issueTemplateId')
    artifact_next_action_id = invocation.get('artifactNextActionId')
    issue_bindings = {item.get('templateId'): item for item in contract.get('issueTemplateBindings', []) if isinstance(item, dict)}
    next_actions = {item.get('actionId'): item for item in contract.get('artifactNextActions', []) if isinstance(item, dict)}
    artifact_contracts = {item.get('artifactType'): item for item in contract.get('artifactContracts', []) if isinstance(item, dict)}

    workflow_enum = enum_values(schema, 'properties', 'workflowId')
    trigger_enum = enum_values(schema, 'properties', 'triggerSource')
    execution_mode_enum = enum_values(schema, 'properties', 'executionMode')
    capability_enum = enum_values(schema, 'properties', 'requiredCapabilities', 'items')
    gate_enum = enum_values(schema, '$defs', 'gateSet', 'properties', 'required', 'items')
    mcp_enum = enum_values(schema, '$defs', 'mcpRequirement', 'properties', 'name')
    issue_template_enum = enum_values(schema, 'properties', 'issueTemplateId')
    artifact_next_action_enum = enum_values(schema, 'properties', 'artifactNextActionId')

    if workflow_id in workflow_enum:
        add(checks, 'artifactChecks', 'workflow-id-known', 'passed', 'Workflow id is known.', workflowId=workflow_id)
    else:
        add(checks, 'artifactChecks', 'workflow-id-known', 'blocked', 'Workflow id is not known.', workflowId=workflow_id)

    if trigger_source in trigger_enum:
        add(checks, 'artifactChecks', 'trigger-source-known', 'passed', 'Trigger source is known.', triggerSource=trigger_source)
    else:
        add(checks, 'artifactChecks', 'trigger-source-known', 'blocked', 'Trigger source is not known.', triggerSource=trigger_source)

    if trigger_source == 'issue-template':
        binding = issue_bindings.get(issue_template_id)
        if issue_template_id in issue_template_enum and binding:
            add(checks, 'artifactChecks', 'issue-template-binding-known', 'passed', 'Issue template binding is known.', issueTemplateId=issue_template_id)
            allowed = set(binding.get('allowedWorkflowIds', []))
            if workflow_id in allowed:
                add(checks, 'artifactChecks', 'issue-template-workflow-allowed', 'passed', 'Issue template workflow mapping is allowed.', issueTemplateId=issue_template_id, workflowId=workflow_id)
            else:
                add(checks, 'artifactChecks', 'issue-template-workflow-allowed', 'blocked', 'Issue template workflow mapping is not allowed.', issueTemplateId=issue_template_id, workflowId=workflow_id, allowed=sorted(allowed))
            for artifact_type in binding.get('requiredArtifacts', []):
                if artifact_type in actual_artifacts:
                    add(checks, 'artifactChecks', f'issue-template-artifact:{artifact_type}', 'passed', 'Issue template required artifact is present.', artifactType=artifact_type)
                else:
                    add(checks, 'artifactChecks', f'issue-template-artifact:{artifact_type}', 'blocked', 'Issue template required artifact is missing.', artifactType=artifact_type)
            for gate_id in binding.get('requiredStartGates', []):
                if gate_id in satisfied_gates:
                    add(checks, 'gateChecks', f'issue-template-gate:{gate_id}', 'passed', 'Issue template start gate is satisfied.', gateId=gate_id)
                else:
                    add(checks, 'gateChecks', f'issue-template-gate:{gate_id}', 'blocked', 'Issue template start gate is not satisfied.', gateId=gate_id)
        else:
            add(checks, 'artifactChecks', 'issue-template-binding-known', 'blocked', 'Issue template binding is unknown.', issueTemplateId=issue_template_id)

    if trigger_source == 'artifact-next-action':
        action = next_actions.get(artifact_next_action_id)
        if artifact_next_action_id in artifact_next_action_enum and action:
            add(checks, 'artifactChecks', 'artifact-next-action-known', 'passed', 'Artifact next action is known.', artifactNextActionId=artifact_next_action_id)
            if workflow_id == action.get('suggestedWorkflowId'):
                add(checks, 'artifactChecks', 'artifact-next-action-workflow', 'passed', 'Artifact next action workflow matches suggestion.', artifactNextActionId=artifact_next_action_id, workflowId=workflow_id)
            else:
                add(checks, 'artifactChecks', 'artifact-next-action-workflow', 'blocked', 'Artifact next action workflow does not match suggestion.', artifactNextActionId=artifact_next_action_id, expected=action.get('suggestedWorkflowId'), actual=workflow_id)
            missing_artifacts = sorted(set(action.get('fromArtifactTypes', [])) - actual_artifacts)
            add(checks, 'artifactChecks', 'artifact-next-action-source-artifacts', 'passed' if not missing_artifacts else 'blocked', 'Artifact next action source artifacts must be present.', missing=missing_artifacts)
            missing_gates = sorted(set(action.get('requiredSatisfiedGates', [])) - satisfied_gates)
            add(checks, 'gateChecks', 'artifact-next-action-gates', 'passed' if not missing_gates else 'blocked', 'Artifact next action gates must be satisfied.', missing=missing_gates)
        else:
            add(checks, 'artifactChecks', 'artifact-next-action-known', 'blocked', 'Artifact next action is unknown.', artifactNextActionId=artifact_next_action_id)

    if execution_mode in execution_mode_enum:
        add(checks, 'artifactChecks', 'execution-mode-known', 'passed', 'Execution mode is known.', executionMode=execution_mode)
    else:
        add(checks, 'artifactChecks', 'execution-mode-known', 'blocked', 'Execution mode is not known.', executionMode=execution_mode)

    for artifact_type in actual_artifacts:
        artifact_contract = artifact_contracts.get(artifact_type)
        if artifact_contract:
            add(checks, 'artifactChecks', f'artifact-type-known:{artifact_type}', 'passed', 'Artifact type has a runtime contract.', artifactType=artifact_type, schemaFile=artifact_contract.get('schemaFile'))
        else:
            add(checks, 'artifactChecks', f'artifact-type-known:{artifact_type}', 'blocked', 'Artifact type does not have a runtime contract.', artifactType=artifact_type)

    workflow_contract = contract.get('workflows', {}).get(workflow_id)
    if workflow_contract:
        expected_mode = workflow_contract.get('executionMode')
        if execution_mode == expected_mode:
            add(checks, 'artifactChecks', 'workflow-execution-mode', 'passed', 'Execution mode matches workflow contract.', executionMode=execution_mode)
        else:
            add(checks, 'artifactChecks', 'workflow-execution-mode', 'blocked', 'Execution mode does not match workflow contract.', expected=expected_mode, actual=execution_mode)

        for artifact_type in workflow_contract.get('requiredArtifacts', []):
            if artifact_type in actual_artifacts:
                add(checks, 'artifactChecks', f'artifact:{artifact_type}', 'passed', 'Required artifact type is present.', artifactType=artifact_type)
            else:
                add(checks, 'artifactChecks', f'artifact:{artifact_type}', 'blocked', 'Required artifact type is missing.', artifactType=artifact_type)

        for gate_id in workflow_contract.get('requiredGates', []):
            if gate_id not in required_gates:
                add(checks, 'gateChecks', f'gate:{gate_id}', 'passed', 'Workflow-managed gate is not required at invocation start.', gateId=gate_id)
            elif gate_id in satisfied_gates:
                add(checks, 'gateChecks', f'gate:{gate_id}', 'passed', 'Required start gate is declared and satisfied.', gateId=gate_id)
            else:
                add(checks, 'gateChecks', f'gate:{gate_id}', 'blocked', 'Required start gate is not satisfied.', gateId=gate_id)

        workflow_mcp = set(workflow_contract.get('mcpRequirements', []))
        if workflow_mcp:
            declared = workflow_mcp & actual_mcp
            available = workflow_mcp & available_mcp
            if declared and available:
                add(checks, 'mcpChecks', f'mcp:{workflow_id}', 'passed', 'Workflow MCP dependency is declared and available.', requiredAny=sorted(workflow_mcp), available=sorted(available))
            elif declared:
                add(checks, 'mcpChecks', f'mcp:{workflow_id}', 'blocked', 'Workflow MCP dependency is declared but not available.', requiredAny=sorted(workflow_mcp), declared=sorted(declared), available=sorted(available_mcp))
            else:
                add(checks, 'mcpChecks', f'mcp:{workflow_id}', 'blocked', 'Workflow MCP dependency is missing from invocation.', requiredAny=sorted(workflow_mcp))
        else:
            add(checks, 'mcpChecks', f'mcp:{workflow_id}', 'passed', 'Workflow has no specific MCP server dependency.')

    for capability in actual_capabilities:
        if capability not in capability_enum:
            add(checks, 'capabilityChecks', f'capability-known:{capability}', 'blocked', 'Invocation declares an unknown capability.', capability=capability)
        elif capability not in available_capabilities:
            add(checks, 'capabilityChecks', f'capability-available:{capability}', 'blocked', 'Required capability was not provided as available.', capability=capability)
        else:
            add(checks, 'capabilityChecks', f'capability-available:{capability}', 'passed', 'Required capability is available.', capability=capability)

    for capability in contract.get('requiredCapabilities', []):
        if capability in actual_capabilities:
            add(checks, 'capabilityChecks', f'capability-required:{capability}', 'passed', 'Invocation includes runtime-required capability.', capability=capability)
        else:
            add(checks, 'capabilityChecks', f'capability-required:{capability}', 'blocked', 'Invocation omits runtime-required capability.', capability=capability)

    for gate_id in required_gates | satisfied_gates:
        if gate_id in gate_enum:
            add(checks, 'gateChecks', f'gate-known:{gate_id}', 'passed', 'Gate id is known.', gateId=gate_id)
        else:
            add(checks, 'gateChecks', f'gate-known:{gate_id}', 'blocked', 'Gate id is unknown.', gateId=gate_id)

    for name in actual_mcp:
        if name in mcp_enum:
            add(checks, 'mcpChecks', f'mcp-known:{name}', 'passed', 'MCP server id is known.', name=name)
        else:
            add(checks, 'mcpChecks', f'mcp-known:{name}', 'blocked', 'MCP server id is unknown.', name=name)

    if workflow_id == 'writing-plans':
        ok = 'approved-spec' in actual_artifacts and 'spec-approval' in satisfied_gates
        add(checks, 'illegalTransitionChecks', 'planning-requires-approved-spec', 'passed' if ok else 'blocked', 'Planning requires approved spec artifact and spec approval gate.')
    else:
        add(checks, 'illegalTransitionChecks', 'planning-requires-approved-spec', 'passed', 'Rule not applicable to this workflow.')

    if workflow_id in {'executing-plans', 'subagent-driven-development'}:
        missing = sorted({'implementation-plan', 'wiki-context'} - actual_artifacts)
        add(checks, 'illegalTransitionChecks', 'execution-requires-approved-plan-and-wiki-context', 'passed' if not missing else 'blocked', 'Execution requires implementation plan and wiki context.', missing=missing)
    else:
        add(checks, 'illegalTransitionChecks', 'execution-requires-approved-plan-and-wiki-context', 'passed', 'Rule not applicable to this workflow.')

    if workflow_id == 'brainstorming' and 'lanhu-evidence-package' in actual_artifacts:
        ok = 'lanhu-scope-confirmation' in satisfied_gates
        add(checks, 'illegalTransitionChecks', 'lanhu-handoff-requires-confirmation-gate', 'passed' if ok else 'blocked', 'Lanhu evidence handoff requires confirmation gate.')
    else:
        add(checks, 'illegalTransitionChecks', 'lanhu-handoff-requires-confirmation-gate', 'passed', 'Rule not applicable to this invocation.')

    if workflow_id == 'shared-wiki-mcp':
        ok = 'external-pr-creation-authorization' in satisfied_gates
        add(checks, 'illegalTransitionChecks', 'external-side-effects-require-authorization-gate', 'passed' if ok else 'blocked', 'Shared-wiki MCP PR creation requires authorization gate.')
    elif workflow_id == 'publish-shared-wiki':
        ok = 'shared-wiki-publish-authorization' in satisfied_gates
        add(checks, 'illegalTransitionChecks', 'external-side-effects-require-authorization-gate', 'passed' if ok else 'blocked', 'Shared-wiki publish requires authorization gate.')
    else:
        add(checks, 'illegalTransitionChecks', 'external-side-effects-require-authorization-gate', 'passed', 'Rule not applicable to this workflow.')

    intent_text = str(invocation.get('userIntent', '')).lower()
    feature_terms = ('new feature', 'feature', 'behavior change', 'new capability', '新功能', '行为变更', '改行为')
    if workflow_id in {'executing-plans', 'subagent-driven-development'} and any(term in intent_text for term in feature_terms):
        ok = {'approved-spec', 'implementation-plan', 'wiki-context'} <= actual_artifacts and 'spec-approval' in satisfied_gates
        add(checks, 'illegalTransitionChecks', 'feature-work-cannot-start-at-execution', 'passed' if ok else 'blocked', 'Feature or behavior-change execution requires approved spec, plan, wiki context, and spec approval gate.')
    else:
        add(checks, 'illegalTransitionChecks', 'feature-work-cannot-start-at-execution', 'passed', 'Rule not applicable to this invocation.')

    completion_proof_terms = ('completion proof', 'release proof', 'mark complete', 'prove complete', '作为完成', '完成证明', '发布证明')
    if workflow_id == 'update-wiki' and any(term in intent_text for term in completion_proof_terms):
        add(checks, 'illegalTransitionChecks', 'update-wiki-is-not-completion-proof', 'blocked', 'update-wiki maintenance cannot be used as implementation completion, verification, or release proof.')
    else:
        add(checks, 'illegalTransitionChecks', 'update-wiki-is-not-completion-proof', 'passed', 'Rule not applicable to this invocation.')

    status = 'blocked' if any(item['status'] == 'blocked' for category in checks.values() for item in category) else 'passed'
    preflight = {
        'status': status,
        'checkedAt': datetime.now(timezone.utc).replace(microsecond=0).isoformat(),
        **checks,
    }
    print(json.dumps({'status': status, 'preflight': preflight}, ensure_ascii=False, indent=2, sort_keys=True))

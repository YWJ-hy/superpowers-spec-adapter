  if ! grep -Fq -- "$required" "${TARGET_INPUT}/skills/migrate-wiki/references/graph-enrichment.md"; then
    printf 'Expected migrate-wiki enrichment companion to contain: %s\n' "$required" >&2
    exit 1
  fi
done

for deprecated_command in \
  update-wiki \
  init-wiki \
  import-wiki \
  migrate-wiki \
  lanhu-requirements \
  shared-wiki-mcp \
  publish-shared-wiki
do
  deprecated_path="${TARGET_INPUT}/commands/${deprecated_command}.md"
  if [[ -f "$deprecated_path" ]] && grep -Fq 'Generated by superpower-adapter' "$deprecated_path"; then
    printf 'Expected deprecated command overlay to be removed: %s\n' "$deprecated_command" >&2
    exit 1
  fi
done

PUBLISH_SHARED_WIKI_SKILL="${TARGET_INPUT}/skills/publish-shared-wiki/SKILL.md"

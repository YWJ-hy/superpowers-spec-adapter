  exit 1
fi
for required in 'wikiRoots:' '.shared-superpowers/wiki' 'root-prefixed `displayPath`' 'sharedWikiSource: auto' 'github_mcp' 'wikiPath' 'revision' 'logical display path' 'shared-wiki MCP' 'selectionOutputPath' 'Plan-phase return'; do
  if ! grep -Fq -- "$required" "${TARGET_INPUT}/agents/wiki-researcher.md"; then
    printf 'Expected installed wiki-researcher shared-root text: %s\n' "$required" >&2
    exit 1
  fi
done

if [[ ! -f "${TARGET_INPUT}/agents/lanhu-frontend-requirements-analyst.md" ]]; then
  printf 'Expected installed lanhu-frontend-requirements-analyst agent\n' >&2
  exit 1
fi

if [[ -f "${TARGET_INPUT}/agents/lanhu-frontend-html-requirements-analyst.md" ]] && grep -Fq 'Generated by superpower-adapter' "${TARGET_INPUT}/agents/lanhu-frontend-html-requirements-analyst.md"; then
  printf 'Deprecated lanhu-frontend-html-requirements-analyst remains installed\n' >&2
  exit 1
fi

if [[ ! -f "${TARGET_INPUT}/agents/lanhu-backend-requirements-analyst.md" ]]; then
  printf 'Expected installed lanhu-backend-requirements-analyst agent\n' >&2
  exit 1
fi

if [[ -f "${TARGET_INPUT}/agents/source-of-truth-verifier.md" ]] && grep -Fq 'Generated by superpower-adapter' "${TARGET_INPUT}/agents/source-of-truth-verifier.md"; then
  printf 'Deprecated source-of-truth-verifier remains installed\n' >&2
  exit 1
fi

if [[ -f "${TARGET_INPUT}/contracts/source-truth-verification.md" ]] && grep -Fq 'Generated by superpower-adapter' "${TARGET_INPUT}/contracts/source-truth-verification.md"; then
  printf 'Deprecated source-truth verification contract remains installed\n' >&2
  exit 1
fi

if [[ -f "${TARGET_INPUT}/scripts/source_truth_render.py" ]] && grep -Fq 'Generated by superpower-adapter' "${TARGET_INPUT}/scripts/source_truth_render.py"; then
  printf 'Deprecated source_truth_render.py remains installed\n' >&2
  exit 1
fi

for required_script in source_truth_common.py source_truth_settings.py; do
  if [[ ! -f "${TARGET_INPUT}/scripts/${required_script}" ]]; then
    printf 'Expected installed source-truth script: %s\n' "$required_script" >&2
    exit 1
  fi
 done

for required in '--render-prompt' '--lint-changed' '--changed-path' '--changed-paths-file' '--authorized-truth-edit' 'SOT_TRUTH_EDIT_FORBIDDEN' 'SOT_TRUTH_EDIT_AUTH_MISSING' 'SOT_EVIDENCE_CHANGED'; do
  if ! grep -Fq -- "$required" "${TARGET_INPUT}/scripts/source_truth_settings.py"; then
    printf 'Expected source_truth_settings.py to contain policy/lint capability: %s\n' "$required" >&2
    exit 1
  fi
 done

for standalone_skill in \
  init-wiki \
  import-wiki \
  migrate-wiki \
  lanhu-requirements \
  shared-wiki-mcp \
  publish-shared-wiki
do
  if [[ ! -f "${TARGET_INPUT}/skills/${standalone_skill}/SKILL.md" ]]; then
    printf 'Expected installed standalone adapter skill: %s\n' "$standalone_skill" >&2
    exit 1
  fi
done

for file in \
  "${TARGET_INPUT}/skills/using-superpowers/SKILL.md" \
  "${TARGET_INPUT}/skills/brainstorming/SKILL.md" \
  "${TARGET_INPUT}/skills/systematic-debugging/SKILL.md" \
  "${TARGET_INPUT}/skills/writing-plans/SKILL.md" \
  "${TARGET_INPUT}/skills/executing-plans/SKILL.md" \
  "${TARGET_INPUT}/skills/subagent-driven-development/SKILL.md" \
  "${TARGET_INPUT}/skills/finishing-a-development-branch/SKILL.md"
do
  if ! grep -Fq 'Generated by superpower-adapter' "$file"; then
    printf 'Expected adapter native patch marker in %s\n' "$file" >&2
    exit 1
  fi
done

USING_SUPERPOWERS_SKILL="${TARGET_INPUT}/skills/using-superpowers/SKILL.md"
for required in \
  'Adapter Workflow Boundary' \
  'Standalone adapter skills have their own local workflow boundary' \
  'init-wiki' \
  'import-wiki' \
  'migrate-wiki' \
  'lanhu-requirements' \
  'shared-wiki-mcp' \
  'publish-shared-wiki' \
  'update-wiki' \
  'explicit confirmation of the commit/push scope' \
  'Do not automatically invoke Superpowers planning, implementation, review, completion, or verification skills' \
  'do not invoke `superpowers:verification-before-completion` or similar completion checks' \
  'This boundary does not apply to normal Superpowers development workflows' \
  'brainstorming' \
  'writing-plans' \
  'executing-plans' \
  'subagent-driven-development' \
  'systematic-debugging'
do
  if ! grep -Fq -- "$required" "$USING_SUPERPOWERS_SKILL"; then
    printf 'Expected using-superpowers patch to contain adapter workflow boundary: %s\n' "$required" >&2
    exit 1
  fi
done

BRAINSTORMING_SKILL="${TARGET_INPUT}/skills/brainstorming/SKILL.md"
if ! grep -Fq 'wiki-researcher' "$BRAINSTORMING_SKILL"; then
  printf 'Expected brainstorming patch to mention wiki-researcher\n' >&2
  exit 1
fi
for required in 'wikiRoots:' '.shared-superpowers/wiki' 'sharedWikiSource: auto' 'logical display path' 'MCP is unavailable' 'Do not write sidecar JSONL, `.wiki-selection.json`, or `.wiki-context.json` during brainstorming.'; do
  if ! grep -Fq -- "$required" "$BRAINSTORMING_SKILL"; then
    printf 'Expected brainstorming patch to contain shared wiki source text: %s\n' "$required" >&2
    exit 1
  fi
done

for required in \
  'lanhu-requirements skill <Lanhu link> frontend|backend' \
  'Do not run Lanhu intake inside `brainstorming`' \
  'already confirmed `.lanhu/.../index.md` package' \
  'read that `index.md` first' \
  'do not call Lanhu MCP by default' \
  'Lanhu MCP is optional'
do
  if ! grep -Fq -- "$required" "$BRAINSTORMING_SKILL"; then
    printf 'Expected brainstorming patch to keep Lanhu explicit-skill boundary: %s\n' "$required" >&2
    exit 1
  fi
done

for forbidden in \
  'scopePolicy: pageid_children_only' \
  'lanhu_get_prd_scoped_evidence' \
  'deliveryBoundaryPlan' \
  'sourceFactCoverage.sourceFactsDroppedDetected: []' \
  'pagePackageMode: true' \
  'aggregationPolicy: full_package_per_page' \
  'Do not quote, summarize, or pass through tool-returned persona'
do
  if grep -Fq -- "$forbidden" "$BRAINSTORMING_SKILL"; then
    printf 'Expected brainstorming patch not to embed full Lanhu workflow: %s\n' "$forbidden" >&2
    exit 1
  fi
done

WRITING_SKILL="${TARGET_INPUT}/skills/writing-plans/SKILL.md"
if ! grep -Fq 'wiki-researcher' "$WRITING_SKILL"; then
  printf 'Expected writing-plans patch to mention wiki-researcher\n' >&2
  exit 1
fi

for required in \
  'sharedWikiSource: auto' \
  'selectionOutputPath' \
  'wiki-context-v4.example.jsonc' \
  '--validate-only --strict' \
  'infer the JSON format from `scripts/wiki_context_render.py`' \
  'schemaVersion 4 JSON' \
  'page-rooted `wikiPages`' \
  'bounded `documentContext`' \
  'taskWikiRefs' \
  'taskFingerprint' \
  'source_truth_settings.py' \
  '--render-prompt plan-pre' \
  '--render-prompt plan-review' \
  'Do not require a sourceOfTruth verification plan section' \
  'do not create sourceOfTruth sidecar artifacts'
do
  if ! grep -Fq -- "$required" "$WRITING_SKILL"; then
    printf 'Expected writing-plans patch to contain slim strict wiki/source-truth requirement: %s\n' "$required" >&2
    exit 1
  fi
done

for forbidden in \
  'source-of-truth-verifier' \
  'source_truth_render.py' \
  'source-truth-report.json' \
  'source-truth-constraints.json' \
  'contracts/source-truth-verification.md'
do
  if grep -Fq -- "$forbidden" "$WRITING_SKILL"; then
    printf 'Expected writing-plans patch not to contain removed sourceOfTruth flow: %s\n' "$forbidden" >&2
    exit 1
  fi
done

# R8 regression guard: the always-loaded planning patch must not front-load execution-only hard
# reread directives (those belong to executing-plans / subagent-driven-development at run time).
for forbidden in '## Hard Wiki Constraint Rereads' '--reread-list'; do
  if grep -Fq -- "$forbidden" "$WRITING_SKILL"; then
    printf 'Planning patch must not contain execution-only hard reread directive: %s\n' "$forbidden" >&2
    exit 1
  fi
done

SYSTEMATIC_SKILL="${TARGET_INPUT}/skills/systematic-debugging/SKILL.md"
for required in \
  'wiki-researcher' \
  'phase: debug' \
  'sharedWikiSource: auto' \
  'Do not call `wiki-researcher` at the start of debugging' \
  'do not write `.wiki-context.json`' \
  'continue systematic debugging' \
  'break-loop'
do
  if ! grep -Fq -- "$required" "$SYSTEMATIC_SKILL"; then
    printf 'Expected systematic-debugging patch to contain: %s\n' "$required" >&2
    exit 1
  fi
done


if grep -Fq 'planPath:' "$SYSTEMATIC_SKILL"; then
  printf 'Expected systematic-debugging patch not to request a planPath\n' >&2
  exit 1
fi

if grep -Fq 'docs/superpowers/plans/<plan-stem>.wiki-context.json' "$SYSTEMATIC_SKILL"; then
  printf 'Expected systematic-debugging patch not to generate planning wiki context sidecar\n' >&2
  exit 1
fi

if grep -Fq 'Before attempting ANY fix, use wiki-researcher' "$SYSTEMATIC_SKILL"; then
  printf 'Expected systematic-debugging patch not to make wiki-researcher a debugging prerequisite\n' >&2
  exit 1
fi

for required in 'wikiRoots:' '.shared-superpowers/wiki' 'sharedWikiSource: auto' 'schemaVersion: 4' 'page-rooted `wikiPages` tree' 'documentContext' 'taskWikiRefs' 'taskFingerprint' 'destination.tasks' 'sharedWiki' 'shared_wiki_status'; do
  if ! grep -Fq -- "$required" "${TARGET_INPUT}/skills/writing-plans/SKILL.md"; then
    printf 'Expected writing-plans patch to contain source-aware wiki context: %s\n' "$required" >&2
    exit 1
  fi
done

if ! grep -Fq 'Referenced Project Wiki' "${TARGET_INPUT}/skills/writing-plans/SKILL.md"; then
  printf 'Expected writing-plans patch to require Referenced Project Wiki\n' >&2
  exit 1
fi

if ! grep -Fq '.wiki-context.json' "${TARGET_INPUT}/skills/writing-plans/SKILL.md"; then
  printf 'Expected writing-plans patch to require wiki context sidecar\n' >&2
  exit 1
fi

for required in 'categorized constraints' 'implementation' 'test' 'review' 'general'; do
  if ! grep -Fq -- "$required" "${TARGET_INPUT}/skills/writing-plans/SKILL.md"; then
    printf 'Expected writing-plans patch to require categorized constraints: %s\n' "$required" >&2
    exit 1
  fi
done

if ! grep -Fq 'hard constraint status' "${TARGET_INPUT}/skills/writing-plans/SKILL.md"; then
  printf 'Expected writing-plans patch to require hard constraint status\n' >&2
  exit 1
fi

if ! grep -Fq 'Referenced Project Wiki' "${TARGET_INPUT}/skills/executing-plans/SKILL.md"; then
  printf 'Expected executing-plans patch to consume Referenced Project Wiki\n' >&2
  exit 1
fi

if ! grep -Fq '.wiki-context.json' "${TARGET_INPUT}/skills/executing-plans/SKILL.md"; then
  printf 'Expected executing-plans patch to consume wiki context sidecar\n' >&2
  exit 1
fi

for required in 'Adapter Task Context' 'wiki_context_render.py' '--task-id' '--fingerprint-preflight' '--execution-ready' '--role implementer' '--reread-list' 'shared_wiki_read_sections' '--batch-jsonl' 'Worktree first' 'superpowers:using-git-worktrees' 'GIT_DIR != GIT_COMMON' '## Assigned Task' '## Rendered Wiki Constraints for This Task' '## Hard Wiki Constraint Rereads' 'rebinding drift' 'Adapter Source-of-Truth Task Lint' 'source_truth_settings.py' '--render-prompt execution-reminder' '--lint-changed' '--changed-path' '--authorized-truth-edit' 'truth/edit: never' 'truth/edit: ask' 'keep-or-skip determination about durable implementation knowledge' 'and bypass the skill' 'candidate absence is not conclusive'; do
  if ! grep -Fq -- "$required" "${TARGET_INPUT}/skills/executing-plans/SKILL.md"; then
    printf 'Expected executing-plans patch to contain source-aware execution text: %s\n' "$required" >&2
    exit 1
  fi
done

if ! grep -Fq 'Referenced Project Wiki' "${TARGET_INPUT}/skills/subagent-driven-development/SKILL.md"; then
  printf 'Expected subagent-driven-development patch to forward Referenced Project Wiki\n' >&2
  exit 1
fi

if ! grep -Fq '.wiki-context.json' "${TARGET_INPUT}/skills/subagent-driven-development/SKILL.md"; then
  printf 'Expected subagent-driven-development patch to forward wiki context sidecar\n' >&2
  exit 1
fi

for required in 'Adapter Task Context' 'wiki_context_render.py' '--task-id' '--fingerprint-preflight' '--execution-ready' '--role implementer' '--role reviewer' '--reread-list' 'shared_wiki_read_sections' 'revision metadata' '--batch-jsonl' 'Worktree first' 'superpowers:using-git-worktrees' 'GIT_DIR != GIT_COMMON' 'file-handoff model' 'task-brief' 'review-package' '-wiki.md' 'git rev-parse --show-toplevel)/.superpowers/sdd' '## Hard Wiki Constraint Rereads' 'rebinding drift' 'Adapter Source-of-Truth Task Lint' 'source_truth_settings.py' '--render-prompt execution-reminder' '--lint-changed' '--changed-path' '--authorized-truth-edit' 'truth/edit: never' 'truth/edit: ask' 'keep-or-skip determination about durable implementation knowledge' 'and bypass the skill' 'candidate absence is not conclusive'; do
  if ! grep -Fq -- "$required" "${TARGET_INPUT}/skills/subagent-driven-development/SKILL.md"; then
    printf 'Expected subagent-driven-development patch to contain source-aware forwarding text: %s\n' "$required" >&2
    exit 1
  fi
done

# The Controller-Only nested-dispatch block (native escalation covers it now) and the
# inline task-text/wiki paste (file-handoff replaces it) must be gone from SDD.
for forbidden in 'Controller-Only Subagent Orchestration' 'Subagents are leaf workers' '## Assigned Task' '## Rendered Wiki Constraints for This Task'; do
  if grep -Fq -- "$forbidden" "${TARGET_INPUT}/skills/subagent-driven-development/SKILL.md"; then
    printf 'Expected subagent-driven-development patch to no longer contain: %s\n' "$forbidden" >&2
    exit 1
  fi
done

# finishing-a-development-branch carries the adapter durable-knowledge entry gate: the
# update-wiki keep-or-skip determination must be made by running the skill before a
# finishing option removes the worktree. The block leads with the native Step 1 (Verify
# Tests) precondition so it does not displace it.
FINISHING_SKILL="${TARGET_INPUT}/skills/finishing-a-development-branch/SKILL.md"
for required in \
  'Adapter Durable-Knowledge Gate' \
  'Step 1 (Verify Tests) still comes first' \
  'keep-or-skip determination about durable implementation knowledge' \
  'do not pre-decide in the main loop that nothing is durable and bypass the skill' \
  'candidate absence is not conclusive' \
  'last reliable point to consume it' \
  'never blocks a finishing option once the determination is made'
do
  if ! grep -Fq -- "$required" "$FINISHING_SKILL"; then
    printf 'Expected finishing-a-development-branch durable-knowledge gate: %s\n' "$required" >&2
    exit 1
  fi
done

# using-git-worktrees stays unpatched.
if [[ -f "${TARGET_INPUT}/skills/using-git-worktrees/SKILL.md" ]] && grep -Fq 'superpower-adapter:native-skill' "${TARGET_INPUT}/skills/using-git-worktrees/SKILL.md"; then
  printf 'Adapter must no longer patch using-git-worktrees\n' >&2
  exit 1
fi

